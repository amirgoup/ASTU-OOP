public class DataTypesAndLiterals {
    public static void main(String[] args) {
        // Integer literals
        byte b = 100;
        short s = 10000;
        int i = 100000;
        long l = 1000000000L;

        // Floating point literals
        float f = 10.5f;
        double d = 10.5;

        // Character literal
        char c = 'a';

        // Boolean literal
        boolean bool = true;

        System.out.println("Byte Literal: " + b);
        System.out.println("Short Literal: " + s);
        System.out.println("Integer Literal: " + i);
        System.out.println("Long Literal: " + l);
        System.out.println("Floating Point Literal: " + f);
        System.out.println("Double Literal: " + d);
        System.out.println("Character Literal: " + c);
        System.out.println("Boolean Literal: " + bool);
    }
}
